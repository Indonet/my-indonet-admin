Due to the changes of our billing system, we hereby send you a softcopy of the newly formatted Invoice. 
This change in Invoice format is effective starting November 1st, 2021.<br>
<br>
The following is an explanation of the Information that is shown in the new Invoice format:<br>
<br>
1.	“Saldo Tagihan Lalu” (Past Invoice Balance) is the balance due from the previous billing period.<br>
2.	Details of transactions that occur in the current period such as Payment Receipts, Administration Fees, service bills for the current period, and others.<br>
3.	“Subtotal (Base amount taxable), is the current period total invoice amount that is subject to state income tax.<br>
4.	“PPN – VAT (10%)” is the amount of state income tax that is being imposed on point 3 above.<br>
5.	“Jumlah Tagihan” (Billing Amount) is the total accumulated outstanding debt from customers that is due.<br>
<br>
This newly formatted Invoice format is generated automatically by Indonet’s Billing System.<br>
